  static TextTheme _buildTextTheme(TextTheme base) {
    return base.copyWith(
      headlineLarge: GoogleFonts.poppins(fontSize: 32, fontWeight: FontWeight.bold),
      headlineMedium: GoogleFonts.poppins(fontSize: 28, fontWeight: FontWeight.bold),
      headlineSmall: GoogleFonts.poppins(fontSize: 24, fontWeight: FontWeight.w600),
      titleLarge: GoogleFonts.poppins(fontSize: 22, fontWeight: FontWeight.bold),
      titleMedium: GoogleFonts.poppins(fontSize: 18),
      titleSmall: GoogleFonts.poppins(fontSize: 16),
      bodyLarge: GoogleFonts.poppins(fontSize: 16, fontWeight: FontWeight.w500),
      bodyMedium: GoogleFonts.poppins(fontSize: 14),
      bodySmall: GoogleFonts.poppins(fontSize: 12),
      labelLarge: GoogleFonts.poppins(fontSize: 14, fontWeight: FontWeight.bold),
    );
  }

  static ThemeData lightTheme() {
    final ThemeData base = ThemeData.light();
    return base.copyWith(
      brightness: Brightness.light,
      splashFactory: NoSplash.splashFactory,
      splashColor: Colors.transparent,
      highlightColor: Colors.transparent,
      colorScheme: ColorScheme.light(
        primary: lightPrimary500,
        onPrimary: Colors.white,
        primaryContainer: lightPrimary100,
        onPrimaryContainer: lightPrimary900,
        secondary: lightSecondary900,
        onSecondary: Colors.white,
        secondaryContainer: lightSecondary100,
        onSecondaryContainer: lightSecondary900,
        tertiary: lightAccent500,
        onTertiary: Colors.white,
        tertiaryContainer: lightAccent100,
        onTertiaryContainer: lightAccent900,
        surface: lightBackground100,
        onSurface: lightText900,
        surfaceContainerHighest: lightBackground200,
        onSurfaceVariant: lightText700,
        error: const Color(0xFFB00020),
        onError: Colors.white,
        outline: lightText300,
        outlineVariant: lightText200,
      ),
      scaffoldBackgroundColor: lightBackground50,
      appBarTheme: AppBarTheme(
        backgroundColor: lightBackground50,
        foregroundColor: lightText900,
        elevation: 0,
      ),
      textTheme: _buildTextTheme(
        GoogleFonts.poppinsTextTheme(
          base.textTheme,
        ).apply(bodyColor: lightText900, displayColor: lightText900),
      ),
      elevatedButtonTheme: ElevatedButtonThemeData(
        style: ElevatedButton.styleFrom(
          backgroundColor: lightPrimary500,
          foregroundColor: lightAccent50,
          iconColor: lightAccent50,
          padding: const EdgeInsets.symmetric(
            horizontal: AppConstants.largeSpacing,
            vertical: AppConstants.mediumSpacing,
          ),
          shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(12)),
          splashFactory: NoSplash.splashFactory,
        ),
      ),
      cardTheme: CardTheme(
        color: lightBackground200,
        elevation: 2,
        shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(16)),
      ),
      dividerTheme: DividerThemeData(color: lightText200, thickness: 1),
      switchTheme: SwitchThemeData(
        thumbColor: WidgetStateProperty.resolveWith<Color>((states) {
          if (states.contains(WidgetState.selected)) {
            return lightPrimary500;
          }
          return lightText400;
        }),
        trackColor: WidgetStateProperty.resolveWith<Color>((states) {
          if (states.contains(WidgetState.selected)) {
            return lightPrimary500.withAlpha(128);
          }
          return lightText400.withAlpha(77);
        }),
      ),
      dialogTheme: DialogTheme(
        backgroundColor: lightBackground100,
        shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(16)),
      ),
      iconTheme: IconThemeData(color: lightPrimary500),
      chipTheme: ChipThemeData(
        backgroundColor: lightSecondary100,
        labelStyle: TextStyle(color: lightSecondary700),
      ),
      bottomSheetTheme: BottomSheetThemeData(
        backgroundColor: lightBackground100,
        shape: RoundedRectangleBorder(borderRadius: BorderRadius.vertical(top: Radius.circular(20))),
      ),
      floatingActionButtonTheme: FloatingActionButtonThemeData(
        backgroundColor: lightPrimary500,
        foregroundColor: Colors.white,
      ),
      inputDecorationTheme: InputDecorationTheme(
        fillColor: lightBackground100,
        filled: true,
        border: OutlineInputBorder(borderRadius: BorderRadius.circular(12), borderSide: BorderSide.none),
        focusedBorder: OutlineInputBorder(
          borderRadius: BorderRadius.circular(12),
          borderSide: BorderSide(color: lightPrimary500, width: 2),
        ),
      ),
    );
  }

